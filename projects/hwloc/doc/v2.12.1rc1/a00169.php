<a name="details" id="details"></a><h2 class="groupheader">Detailed Description</h2>
<p>Memory attribues are identified by an ID (<a class="el" href="a00168.php#gacc82003a8610be554615995f0996c888" title="A memory attribute identifier.">hwloc_memattr_id_t</a>) and a name. <a class="el" href="a00169.php#ga5300e9be1abdea2e00c18492148aecd0" title="Return the name of a memory attribute.">hwloc_memattr_get_name()</a> and <a class="el" href="a00168.php#ga79f44f8e1cfd2f97a19171417a75a137" title="Return the identifier of the memory attribute with the given name.">hwloc_memattr_get_by_name()</a> convert between them (or return error if the attribute does not exist).</p>
<p>The set of valid <a class="el" href="a00168.php#gacc82003a8610be554615995f0996c888" title="A memory attribute identifier.">hwloc_memattr_id_t</a> is a contigous set starting at <code>0</code>. It first contains predefined attributes, as listed in <a class="el" href="a00168.php#gab4bde745db676fc2ea4b47011cd9a80e" title="Predefined memory attribute IDs. See hwloc_memattr_id_t for the generic definition of IDs for predefi...">hwloc_memattr_id_e</a> (from <code>0</code> to <code>HWLOC_MEMATTR_ID_MAX-1</code>). Then custom attributes may be dynamically registered with <a class="el" href="a00169.php#ga770657d1e44b09e93e09f623936c1e5f" title="Register a new memory attribute.">hwloc_memattr_register()</a>. They will get the following IDs (<code>HWLOC_MEMATTR_ID_MAX</code> for the first one, etc.).</p>
<p>To iterate over all valid attributes (either predefined or dynamically registered custom ones), one may iterate over IDs starting from <code>0</code> until <a class="el" href="a00169.php#ga5300e9be1abdea2e00c18492148aecd0" title="Return the name of a memory attribute.">hwloc_memattr_get_name()</a> or <a class="el" href="a00169.php#ga4c62b626085adeaffb233e5f2a03a5eb" title="Return the flags of the given attribute.">hwloc_memattr_get_flags()</a> returns an error.</p>
<p>The values for an existing attribute or for custom dynamically registered ones may be set or modified with <a class="el" href="a00169.php#ga960529c08b25cf15825e0f72ecceb504" title="Set an attribute value for a specific target NUMA node.">hwloc_memattr_set_value()</a>. </p>
