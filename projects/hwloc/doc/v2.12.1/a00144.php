<p>Stringify the type of a given topology object into a human-readable form. </p>
<p>Contrary to <a class="el" href="a00144.php#ga6a38b931e5d45e8af4323a169482fe39" title="Return a constant stringified object type.">hwloc_obj_type_string()</a>, this function includes object-specific attributes (such as the Group depth, the Bridge type, or OS device type) in the output, and it requires the caller to provide the output buffer.</p>
