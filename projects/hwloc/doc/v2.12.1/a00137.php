<p>Most functions in the hwloc API return an integer value. Unless documentated differently, they return 0 on success and -1 on error. Functions that return a pointer type return <code>NULL</code> on error.</p>
<p><code>errno</code> will be set to a meaningful value whenever possible. This includes the usual <code>EINVAL</code> when invalid function parameters are passed or <code>ENOMEM</code> when an internal allocation fails. Some specific <code>errno</code> value are also used, for instance for binding errors as documented in <a class="el" href="a00146.php">CPU binding</a>.</p>
