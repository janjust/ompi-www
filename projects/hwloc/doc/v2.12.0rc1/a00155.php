<p>Find the depth of cache objects matching cache level and type. </p>
<p>Return the depth of the topology level that contains cache objects whose attributes match <code>cachelevel</code> and <code>cachetype</code>.</p>
<p>This function is identical to calling <a class="el" href="a00143.php#ga8bec782e21be313750da70cf7428b374" title="Returns the depth of objects of type type.">hwloc_get_type_depth()</a> with the corresponding type such as <a class="el" href="a00140.php#ggacd37bb612667dc437d66bfb175a8dc55afa834a85d9e53836cf0db6d0bd8329b4" title="Level 1 instruction Cache (filtered out by default).">HWLOC_OBJ_L1ICACHE</a>, except that it may also return a Unified cache when looking for an instruction cache.</p>
<dl class="section return"><dt>Returns</dt><dd>the depth of the unique matching unified cache level is returned if <code>cachetype</code> is <a class="el" href="a00140.php#gga791c9875c8fe20f3e1e5871e0657e59ba3900b3b2db54941aac249e5a638a2d7a" title="Unified cache.">HWLOC_OBJ_CACHE_UNIFIED</a>.</dd>
<dd>
the depth of either a matching cache level or a unified cache level if <code>cachetype</code> is <a class="el" href="a00140.php#gga791c9875c8fe20f3e1e5871e0657e59bacac60ecad4206f85aeb79bef1604b488" title="Data cache.">HWLOC_OBJ_CACHE_DATA</a> or <a class="el" href="a00140.php#gga791c9875c8fe20f3e1e5871e0657e59ba6f98b0d422b38ba90c5f5c79a11b0658" title="Instruction cache (filtered out by default).">HWLOC_OBJ_CACHE_INSTRUCTION</a>.</dd>
<dd>
the depth of the matching level if <code>cachetype</code> is <code>-1</code> but only one level matches.</dd>
<dd>
<a class="el" href="a00143.php#ggaf4e663cf42bbe20756b849c6293ef575ae99465995cacde6c210d5fc2e409798c" title="Objects of given type exist at different depth in the topology (only for Groups).">HWLOC_TYPE_DEPTH_MULTIPLE</a> if <code>cachetype</code> is <code>-1</code> but multiple levels match.</dd>
<dd>
<a class="el" href="a00143.php#ggaf4e663cf42bbe20756b849c6293ef575a0565ab92ab72cb0cec91e23003294aad" title="No object of given type exists in the topology.">HWLOC_TYPE_DEPTH_UNKNOWN</a> if no cache level matches. </dd></dl>

