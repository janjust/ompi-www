<p>Get the CPU set of logical processors that are physically close to AMD GPU device whose index is <code>dv_ind</code>. </p>
<p>Store in <code>set</code> the CPU-set describing the locality of the AMD GPU device whose index is <code>dv_ind</code>.</p>
<p>Topology <code>topology</code> and device <code>dv_ind</code> must match the local machine. I/O devices detection and the ROCm SMI component are not needed in the topology.</p>
<p>The function only returns the locality of the device. If more information about the device is needed, OS objects should be used instead, see <a class="el" href="a00180.php#gaba05bf9710655bb5b1439bee654340ba" title="Get the hwloc OS device object corresponding to AMD GPU device, whose index is dv_ind.">hwloc_rsmi_get_device_osdev()</a> and <a class="el" href="a00180.php#ga507d0acdd5e9ac374a8d120d59604c80" title="Get the hwloc OS device object corresponding to the AMD GPU device whose index is dv_ind.">hwloc_rsmi_get_device_osdev_by_index()</a>.</p>
<p>This function is currently only implemented in a meaningful way for Linux; other systems will simply get a full cpuset.</p>
<dl class="section return"><dt>Returns</dt><dd>0 on success. </dd>
<dd>
-1 on error, for instance if device information could not be found. </dd></dl>

